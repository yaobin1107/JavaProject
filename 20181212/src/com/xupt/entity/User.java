package com.xupt.entity;

public class User {
	public int a;
	protected int b;
	int c;
	private int d;
	
	
	public static void main(String[] args) {
		User u=new User();
		System.out.println(u.a);
		System.out.println(u.b);
		System.out.println(u.c);
		System.out.println(u.d);
	}
	
}
